/**
 * Un enum qui représente une réponse Oui / Non à une question.
 */
export enum ReponseBinaire {
  OUI = 'OUI',
  NON = 'NON'
}

/**
 * Un enum qui représente une réponse Oui / Non / Je ne sais pas à une question.
 */
export enum ReponseProgressive {
  OUI     = 'OUI',
  NON     = 'NON',
  INCONNU = 'INCONNU'
}
